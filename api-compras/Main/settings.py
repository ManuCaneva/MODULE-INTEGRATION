def _normalize_prefix(value: str | None) -> str:
    if not value:
        return ""
    prefix = value.strip()
    if not prefix:
        return ""
    if not prefix.startswith("/"):
        prefix = "/" + prefix
    if prefix != "/":
        prefix = prefix.rstrip("/")
    return prefix

