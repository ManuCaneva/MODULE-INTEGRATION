def build_prefixed_path(path: str = "/") -> str:
    """Construct a path that includes the configured application prefix."""
    segment = path or "/"
    if not segment.startswith("/"):
        segment = "/" + segment
    if not APP_PATH_PREFIX:
        return segment
    normalized_prefix = APP_PATH_PREFIX.rstrip("/")
    suffix = segment.lstrip("/")
    if not suffix:
        return f"{normalized_prefix}/"
    return f"{normalized_prefix}/{suffix}"

